     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $task = new task();
        $task->ListItem = request('ListItem');
        $task->isComplete = 0;
        $task->isCleared = 0;        
        $task->save();
        return redirect('/tasks');
    }

